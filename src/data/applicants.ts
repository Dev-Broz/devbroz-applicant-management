// Generate application responses based on candidate info
const generateApplicationResponses = (
  name: string,
  category: string,
  experience: string,
  skills: string[],
  employmentType: string
): ApplicationResponse[] => {
  const motivationAnswers = [
    `I am deeply passionate about ${category.toLowerCase()} and believe my skills can make a significant impact in this field. Having worked in related areas, I've developed a strong foundation that I'm eager to apply.`,
    `My journey into ${category.toLowerCase()} started during my academic years, and I've been committed to continuous learning and professional development ever since.`,
    `The intersection of technology and sustainability in ${category.toLowerCase()} excites me. I want to be part of solutions that address real-world challenges.`,
  ];

  const strengthAnswers = [
    `My key strengths include ${skills.slice(0, 2).join(' and ')}, combined with strong analytical and problem-solving abilities. I excel at translating complex technical concepts for diverse audiences.`,
    `I bring expertise in ${skills[0]} along with excellent communication skills. My ability to work collaboratively across teams has been consistently praised by colleagues.`,
    `Strategic thinking and attention to detail are my core strengths. With expertise in ${skills.join(', ')}, I can deliver high-quality results under pressure.`,
  ];

  const challengeAnswers = [
    `In my previous role, I led a challenging project that required coordinating multiple stakeholders with conflicting priorities. Through careful communication and structured approach, we delivered on time.`,
    `I once had to quickly adapt to new technology requirements mid-project. By dedicating extra hours to learning and leveraging team expertise, we successfully completed the initiative.`,
    `Managing a project with a tight deadline and limited resources taught me the importance of prioritization. I developed a phased approach that ensured critical deliverables were met.`,
  ];

  const goalAnswers = [
    `My goal is to grow into a leadership role within ${category.toLowerCase()}, where I can mentor others while contributing to impactful projects that drive sustainable change.`,
    `I aim to become a subject matter expert in ${skills[0]} while expanding my knowledge across adjacent areas. I'm particularly interested in innovation and emerging technologies.`,
    `I want to build a career that combines technical expertise with strategic impact. Eventually, I'd like to lead initiatives that shape industry best practices.`,
  ];

  const availabilityAnswers = {
    'Full-time': 'I am available to start within 2-4 weeks, depending on the final offer date. I am committed to a full-time role and can dedicate my complete attention to this position.',
    'Freelance': 'I have flexibility in my schedule and can begin projects immediately. I typically allocate 20-30 hours per week for consulting engagements and can adjust based on project needs.',
  };

  const idx = name.length % 3;

  return [
    {
      question: 'Why are you interested in this position?',
      answer: motivationAnswers[idx],
    },
    {
      question: 'What are your key strengths that make you suitable for this role?',
      answer: strengthAnswers[idx],
    },
    {
      question: 'Describe a challenging project you\'ve worked on and how you handled it.',
      answer: challengeAnswers[idx],
    },
    {
      question: 'What are your career goals for the next 3-5 years?',
      answer: goalAnswers[idx],
    },
    {
      question: 'What is your availability and preferred work arrangement?',
      answer: availabilityAnswers[employmentType as 'Full-time' | 'Freelance'],
    },
    {
      question: 'How do you stay updated with industry trends and developments?',
      answer: `I regularly follow industry publications, attend conferences when possible, and participate in professional networks. I'm also an active member of relevant online communities and pursue certifications to keep my skills current.`,
    },
  ];
};

// Generate additional profile fields
const generateProfileDetails = (idx: number, experience: string) => {
  const companies = [
    'GreenTech Solutions', 'Energy Dynamics Corp', 'Sustainable Futures Inc',
    'CleanPower Associates', 'EcoConsult Group', 'Renewable Ventures',
    'Carbon Zero Ltd', 'BrightEnergy Co', 'NextGen Power', 'EnergyWise Consulting'
  ];
  
  const universities = [
    'Stanford University - MS Environmental Engineering',
    'MIT - MBA Energy Management',
    'UC Berkeley - BS Mechanical Engineering',
    'Cornell University - MS Sustainable Energy',
    'University of Texas - BS Electrical Engineering',
    'Columbia University - MPA Energy Policy',
    'Georgia Tech - MS Industrial Engineering',
    'Duke University - MBA Strategy',
  ];

  const salaries = ['$80,000 - $100,000', '$100,000 - $120,000', '$120,000 - $150,000', '$150,000 - $180,000', '$180,000+'];
  const noticePeriods = ['Immediate', '2 weeks', '1 month', '2 months', '3 months'];

  const experienceLevel = experience === 'Fresher' ? 0 : experience === '5-10 Years' ? 1 : experience === '10-15 Years' ? 2 : 3;

  return {
    currentCompany: experience === 'Fresher' ? undefined : companies[idx % companies.length],
    education: universities[idx % universities.length],
    linkedIn: `https://linkedin.com/in/profile-${idx}`,
    portfolio: idx % 3 === 0 ? `https://portfolio-${idx}.com` : undefined,
    expectedSalary: salaries[Math.min(experienceLevel, salaries.length - 1)],
    noticePeriod: noticePeriods[idx % noticePeriods.length],
    summary: `Dedicated professional with expertise in sustainable energy solutions. Proven track record of delivering results in complex projects. Strong communicator with ability to work effectively across diverse teams.`,
  };
};

